    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
       String produce = searchTextField.getText().trim();
        if (produce.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a produce name.");
            return;
        }
        List<String> farms = Farm2TableBackend.searchProduce(produce);
        resultTextArea.setText("");
        if (farms.isEmpty()) {
            resultTextArea.setText("No local farms found for " + produce);
        } else {
            for (String farm : farms) {
                resultTextArea.append(farm + "\n\n");
            }
        }

    }//GEN-LAST:event_searchButtonActionPerformed

