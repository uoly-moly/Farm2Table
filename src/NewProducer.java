    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText().trim();
        String productType = jTextField2.getText().trim();
        String phone = jTextField3.getText().trim();
        String location = jTextField4.getText().trim();
        // --- GETTING EMAIL FROM NEW TEXT FIELD ---
        String email = jTextField5.getText().trim();

        if (name.isEmpty() || productType.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name and Product Type are required fields.", "Input Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        boolean success = Farm2TableBackend.addFarmer(name, productType, phone, location, email);

        if (success) {
            JOptionPane.showMessageDialog(this, "Farmer added successfully!");
            this.dispose();
            new Farm2TableFrontend().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add farmer to the database.", "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
                                       

